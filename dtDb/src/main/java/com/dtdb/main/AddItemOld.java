    private void addCalculatePercent() {
        if (jTextField3.getText().length() != 0 && jTextField2.getText().length() != 0) {
            double addPercentCurrent = Integer.parseInt(jTextField2.getText());
            double addPercentDesired = Integer.parseInt(jTextField3.getText());
            double addPercent = (addPercentCurrent / addPercentDesired);
            double addPercentDisp = addPercent * 100;
            System.out.println(addPercent);
            
            jLabel1.setText("Stock Percentage: "+Math.round(addPercentDisp)+"%");
            int addColorGreen = (int) Math.round(addPercent*255);
            int addColorRed = (int) Math.round(255-(addPercent*255));
            System.out.println(addColorRed+" "+addColorGreen);
            Color customColor = new Color(addColorRed,addColorGreen,0);
            jLabel1.setForeground(customColor);
        } else {
            jLabel1.setText("Stock Percentage: "+"0"+"%");
        }
    }
    
